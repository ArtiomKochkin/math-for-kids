const select = document.querySelector(".language__list");
let allLanguages = ["ru", "en"];

select.addEventListener("change", changeURLLanguage);
changeLanguage();

/* перенаправить на url с указанием языка */
function changeURLLanguage() {
    let lang = select.value;
    location.href = window.location.pathname + "#" + lang;
    location.reload();
}

function changeLanguage() {
    let hash = window.location.hash;
    hash = hash.substring(1);
    if (!allLanguages.includes(hash)) {
        location.href = window.location.pathname + "#ru";
        location.reload();
    }
    select.value = hash;
    for (let key in langArray) {
        let element = document.querySelector(".lang-" + key);
        if (element) {
            element.innerHTML = langArray[key][hash];
        }
    }
}